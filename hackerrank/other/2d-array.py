# Complete the hourglassSum function below.
def hourglassSum(arr):

    result = [] 
    for i in range(0, 4):
        
        for j in range(0, 4):
          result.append(arr[i][j] + arr[i][j+1] + arr[i][j+2] + arr[i+1][j+1] + arr[i+2][j] + arr[i+2][j+1] + arr[i+2][j+2])
    
    return max(result)
